public class UnaryOperators {

    public static void main(String[] args) {
        
        // 5 + 6 here 5 and 6 is operand and + is an operator
        //unary : in which single operand required
        //eg. ++ , -- 

        //increment
        int num=10;
        num++;
        System.out.println("num = "+num);
        
        //decrement
        int num2=10;
        num2--;
        System.out.println("num2 = "+num2);

    }
}